entity bai1 is
    Port ( A : in STD_LOGIC;
           B : in STD_LOGIC;
           C : in STD_LOGIC;
           F : out STD_LOGIC);
end bai1;

architecture Behavioral of bai1 is

begin
F <= ((not A nand B) nand ( (A nand not B) nand C));

end Behavioral;
